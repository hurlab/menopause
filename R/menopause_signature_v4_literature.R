cat("  1. Hormone pathways (estrogen/progesterone) central\n")
cat("  2. Public datasets may require phenotype reconstruction\n")
cat("  3. Cell-type and cohort differences can dominate\n\n")

cat("Output files:\n")
cat(sprintf("  Literature review: %s\n", file.path(tab_dir, "literature_review.md")))
cat(sprintf("  Signature comparison: %s\n", file.path(tab_dir, "signature_comparison.tsv")))

